
package com.basicprograms;

public class IncreandDecreOp {


	public static void main(String[] args) {


		int a=10,b=20;


		System.out.println(a++ +  ++b );  //31  // a=10 and b=21 so a+b i.e 10+21 =21

		System.out.println(++a + ++b);   // now, a=11 along with ++a i.e a=12 and b=22 so a+b i.e 12+22 =34


	}
}
